export type Lang = "it" | "en";
const dicts = { it, en };

let lang: Lang = (localStorage.getItem("fvb_lang") as Lang) || (navigator.language.startsWith("it") ? "it" : "en");

export function setLang(l: Lang): void {
  lang = l;
  localStorage.setItem("fvb_lang", l);
}

export function getLang(): Lang {
  return lang;
}

export function t(key: keyof typeof it, vars?: Record<string, string | number>): string {
  let s: string = dicts[lang][key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, String(v));
  return s;
}
